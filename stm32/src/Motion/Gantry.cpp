void Gantry::pauseMotion() {
    step_period_timer->CR1 &= ~TIM_CR1_CEN;
    step_period_timer->CNT = 0;

    step_intermission_timer->CR1 &= ~TIM_CR1_CEN;
    step_intermission_timer->CNT = 0;
}
